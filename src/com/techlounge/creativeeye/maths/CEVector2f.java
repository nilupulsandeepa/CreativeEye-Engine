package com.techlounge.creativeeye.maths;

public class CEVector2f {
    private float x;
    private float y;

    public CEVector2f(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
