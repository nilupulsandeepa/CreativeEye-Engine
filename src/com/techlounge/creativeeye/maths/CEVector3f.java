package com.techlounge.creativeeye.maths;

public class CEVector3f {
    //x,y,z coordinates
    private float x;
    private float y;
    private float z;

    public CEVector3f(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}
